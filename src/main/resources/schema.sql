DROP TABLE IF EXISTS film CASCADE;
DROP TABLE IF EXISTS user_info CASCADE;
DROP TABLE IF EXISTS friendship CASCADE;
DROP TABLE IF EXISTS genre CASCADE;
DROP TABLE IF EXISTS likes CASCADE;
DROP TABLE IF EXISTS rating CASCADE;
DROP TABLE IF EXISTS film_genre CASCADE;

CREATE TABLE IF NOT EXISTS rating (
  rating_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(200) NOT NULL UNIQUE,
  description VARCHAR DEFAULT ''
);

CREATE TABLE IF NOT EXISTS film (
  film_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR NOT NULL,
  description VARCHAR(200) DEFAULT '',
  duration INTEGER NOT NULL,
  release_date DATE NOT NULL,
  rating_id INTEGER
);
ALTER TABLE film ADD FOREIGN KEY (rating_id) REFERENCES rating (rating_id) ON DELETE SET NULL;

CREATE TABLE IF NOT EXISTS user_info (
  user_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR,
  login VARCHAR(100) NOT NULL UNIQUE,
  email VARCHAR(320) NOT NULL UNIQUE,
  birthday DATE NOT NULL
);

CREATE TABLE IF NOT EXISTS genre (
  genre_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(100) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS film_genre (
  film_id INTEGER,
  genre_id INTEGER,
  CONSTRAINT film_genre_pk PRIMARY KEY (film_id, genre_id)
);

ALTER TABLE film_genre ADD FOREIGN KEY (genre_id) REFERENCES genre (genre_id)ON DELETE CASCADE;
ALTER TABLE film_genre ADD FOREIGN KEY (film_id) REFERENCES film (film_id)ON DELETE CASCADE;

CREATE TABLE IF NOT EXISTS friendship (
  user_id INTEGER,
  friend_id INTEGER,
  friendship_state boolean DEFAULT false,
  CONSTRAINT friends_pk PRIMARY KEY (user_id, friend_id)
);

ALTER TABLE friendship ADD FOREIGN KEY (user_id) REFERENCES user_info (user_id) ON DELETE CASCADE;
ALTER TABLE friendship ADD FOREIGN KEY (friend_id) REFERENCES user_info (user_id)ON DELETE CASCADE;

CREATE TABLE IF NOT EXISTS likes (
  film_id INTEGER,
  user_id INTEGER,
  CONSTRAINT likes_pk PRIMARY KEY (film_id, user_id)
);

ALTER TABLE likes ADD FOREIGN KEY (user_id) REFERENCES user_info (user_id)ON DELETE CASCADE;
ALTER TABLE likes ADD FOREIGN KEY (film_id) REFERENCES film (film_id)ON DELETE CASCADE;


